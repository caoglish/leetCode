const fourSum1 = require('../solution/fourSum').fourSum1;
const fourSum = require('../solution/fourSum').fourSum2;


test('fourSum1', () => {
	expect(fourSum1([1, 0, -1, 0, -2, 2], 0)).toEqual([
		[-2, -1, 1, 2],
		[-2, 0, 0, 2],
		[-1, 0, 0, 1]
	]);
	expect(fourSum1([-3, -2, -1, 0, 0, 1, 2, 3], 0)).toEqual([
		[-3, -2, 2, 3],
		[-3, -1, 1, 3],
		[-3, 0, 0, 3],
		[-3, 0, 1, 2],
		[-2, -1, 0, 3],
		[-2, -1, 1, 2],
		[-2, 0, 0, 2],
		[-1, 0, 0, 1]
	]);


});

